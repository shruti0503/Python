# Constants:

# ->In Python, there is no strict concept of constants 
# as in some other programming languages.

# ->Conventionally, variables whose values are not intended 
# to be changed are sometimes referred to as constants.

# ->By convention, names of constants are often written in uppercase to indicate that they are intended not to be modified, but this is not enforced by the language.
# Example of using uppercase for constants by convention:


PI = 3.14159
GRAVITY = 9.8
# It's important to note that Python does not enforce the immutability of variables declared as constants. The convention is followed to indicate the programmer's intention that the value should not be modified.

# In summary, literals are fixed values represented directly in code, while constants, in the context of Python, are often represented by variables whose values are not intended to be changed, following a naming convention.

